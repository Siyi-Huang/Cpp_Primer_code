/*
 * 练习1.17:
 * 如果输入的所有值都是相等的，本节的程序会输出什么？
 * 如果没有重复值，输出又会是怎样的？
 * 
 * 相等值：输出输入值的数量
 * 没有重复值：输出每个数1次
 * 
 * 如果输入的值都相等，则while循环的else分支永远不会执行，
 * 直到输入结束，while循环退出，循环后的输出语句打印这唯一的一个值和它出现的次数。
 * 
 * 若没有重复值，则while循环中的if语句的真值分支永远不会执行，
 * 每读入一个值，都会进入else分支，打印它的值和出现次数1。
 * 输入结束后，while循环退出，循环后的输出语句打印最后一个值和出现次数1。
 */